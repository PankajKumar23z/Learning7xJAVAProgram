package ex_13072024;

public class Lab045_Relational_Comparison_Operator {

    public static void main(String[] args) {

        System.out.println( 10 ==10 ); // == comparison operator
        System.out.println( 10 >=10 ); // 10> 10 or 10 = 10
        System.out.println( 10 <=10 ); // 10< 10 or 10 = 10
        System.out.println( 10 > 10 ); // ==
        System.out.println( 10 < 10 ); // ==

    }
}
