package ex_13072024;

public class Lab018_print_char {

    public static void main(String[] args) {

      //  byte b= 128;  // byte can store from -127 to 127
        // if you switch power mode you can see no error and not able to execute

        char c = 'A';
        System.out.println(c);

        // It will print A

        char c1 = '$';
        System.out.println(c1);

        // It will print $

        char c2 = '\n';
        System.out.println(c2);

        // It will print \n

        char c3 = '\u1F6A';
        System.out.println(c3);

        // It will print unicode characters also







    }
}
