package ex_13072024;

public class Lab041_Compound_Assignment_Operators {

    public static void main(String[] args) {
        // Compound Assignment Operators

        int a = 10;

        // += , -=, /=, *= , %=
        // += ->

        a+=10; // a = a+10
        a-=10; // a = a-10
        a/=10; // a = a/10
        a*=10; // a = a*10







    }
}
