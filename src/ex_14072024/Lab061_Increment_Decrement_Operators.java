package ex_14072024;

public class Lab061_Increment_Decrement_Operators {
    public static void main(String[] args) {

        int a = 10;
        int result = a++;  // Post Increment
        System.out.println(a); // after the line 7 a will be incremented to 11

        // Exp =10,   a=11


        int a1 = 10;
        int result1 = ++a1; // Pre Increment
        System.out.println(a1);


        // Exp =11,   a=11
    }
}
